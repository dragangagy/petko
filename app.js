function updateKeyStates(guess) {
  targets.forEach((target) => {
    scoreGuess(guess, target).forEach((state, index) => {
      const letter = guess[index];
      const old = keyStates.get(letter);
      if (old === "correct") return;
      if (old === "present" && state === "absent") return;
      keyStates.set(letter, state);
    });
  });
}

function scoreGuess(guess, target) {
  const result = Array(WORD_LENGTH).fill("absent");
  const remaining = target.split("");

  for (let index = 0; index < WORD_LENGTH; index += 1) {
    if (guess[index] === target[index]) {
      result[index] = "correct";
      remaining[index] = null;
    }
  }

  for (let index = 0; index < WORD_LENGTH; index += 1) {
    if (result[index] === "correct") continue;
    const foundAt = remaining.indexOf(guess[index]);
    if (foundAt !== -1) {
      result[index] = "present";
      remaining[foundAt] = null;
    }
  }

  return result;
}

