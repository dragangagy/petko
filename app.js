function pressKey(key) {
  if (done) return;
  if (key === "back") {
    current = current.slice(0, -1);
  } else if (key === "enter") {
    submitGuess();
  } else if (/^[абвгдђежзијклљмнњопрстћуфхцчџш]$/.test(key) && current.length < WORD_LENGTH) {
    current += key;
    markNormalStarted();
  }
  render();
  saveCompetitiveProgress();
  saveNormalProgress();
}

function submitGuess() {
  if (current.length !== WORD_LENGTH) {
    messageEl.textContent = "Треба тачно 5 слова.";
    return;
  }

  if (!WORD_SET.has(current)) {
    const missingWord = current;
    current = "";
    messageEl.textContent = "Нема те речи.";
    showMissingWordReview(missingWord);
    render();
    saveNormalProgress();
    return;
  }

  guesses.push(current);
  updateSolvedAndScore(current, guesses.length);
  updateKeyStates(current);
  current = "";
  saveCompetitiveProgress();
  saveNormalProgress();

  const solvedCount = solvedAt.filter(Boolean).length;
  const allSolved = solvedCount === targets.length;

  if (allSolved) {
    done = true;
    if (gameType === "competitive") {
      competitiveCompleted = Math.max(competitiveCompleted, competitiveLevelIndex + 1);
      applyLevelAward();
      const isFinalLevel = competitiveLevelIndex === COMPETITIVE_LEVELS.length - 1;
      nextLevelButton.hidden = isFinalLevel;
      messageEl.textContent = isFinalLevel
        ? finalMessage("finished")
        : levelAwardMessage();
      saveCompetitiveProgress();
      if (isFinalLevel) {
        finishCompetitive("finished");
      }
      updateModeButtons();
    } else if (gameType === "challenge") {
      finishChallenge("finished");
    } else {
      bumpNormalFinished();
      const fridayText = recordFridayNormalWin();
      messageEl.textContent = fridayText || "Погођено!";
      clearNormalProgress();
      nextLevelButton.textContent = "Следећа";
      nextLevelButton.hidden = false;
      renderWordReveal();
    }
  } else if (guesses.length >= maxAttempts()) {
    done = true;
    if (gameType === "competitive") {
      applyFailurePenalty();
      renderSolutionsPanel(true);
      messageEl.textContent = finalMessage("failed");
      finishCompetitive("failed");
    } else if (gameType === "challenge") {
      finishChallenge("failed");
    } else {
      messageEl.textContent = `Реч је била: ${displayWords(targets)}`;
      resetFridayNormalStreak();
      clearNormalProgress();
      nextLevelButton.textContent = "Следећа";
      nextLevelButton.hidden = false;
      renderWordReveal();
    }
  } else {
    messageEl.textContent = gameType === "competitive"
      ? `${solvedCount}/${mode} решено. Скор важи само за такмичарски.`
      : "Настави.";
    saveCompetitiveProgress();
    saveNormalProgress();
  }
}

