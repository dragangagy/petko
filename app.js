function dayNumber(dateText) {
  const date = new Date(`${dateText}T00:00:00`);
  return Math.floor(date.getTime() / 86400000);
}

function currentStreakFor(status) {
  if (status !== "finished") return 0;
  let streak = 1;
  let expected = dayNumber(todayId()) - 1;
  const previous = loadResults()
    .filter((result) => result.date !== todayId() && isFinalResult(result))
    .sort((a, b) => b.date.localeCompare(a.date));

  for (const result of previous) {
    const day = dayNumber(result.date);
    if (day !== expected) break;
    if (result.status !== "finished") break;
    streak += 1;
    expected -= 1;
  }

  return streak;
}

function todayResult() {
  return loadResults().find((result) => result.date === todayId() && isFinalResult(result));
}

function todayLock() {
  try {
    const lock = JSON.parse(localStorage.getItem(LOCK_STORAGE_KEY) || "null");
    if (lock && lock.date === todayId() && isFinalResult(lock)) return lock;
  } catch {}

  const existing = loadResults().find((result) => result.date === todayId() && isFinalResult(result));
  return existing || null;
}

