function unusedBonus() {
  return unusedAttempts() * UNUSED_ATTEMPT_BONUS;
}

function unusedAttempts() {
  return Math.max(0, maxAttempts() - guesses.length);
}

function applyLevelAward() {
  const base = levelPoints();
  const attemptsLeft = unusedAttempts();
  const bonus = unusedBonus();
  const total = base + bonus;
  const before = score;
  score += total;
  lastLevelAward = { base, bonus, attemptsLeft, total, before, after: score };
  bonusFlashRows = new Set();
  animateScoreAward(lastLevelAward);
}

function applyFailurePenalty() {
  score += COMPETITIVE_FAIL_PENALTY;
  updateScoreDisplay();
  showScoreBurst(String(COMPETITIVE_FAIL_PENALTY), "red");
}

