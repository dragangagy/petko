function normalize(text) {
  const latin = {
    a: "а",
    b: "б",
    c: "ц",
    č: "ч",
    ć: "ћ",
    d: "д",
    đ: "ђ",
    e: "е",
    f: "ф",
    g: "г",
    h: "х",
    i: "и",
    j: "ј",
    k: "к",
    l: "л",
    m: "м",
    n: "н",
    o: "о",
    p: "п",
    r: "р",
    s: "с",
    š: "ш",
    t: "т",
    u: "у",
    v: "в",
    z: "з",
    ž: "ж"
  };

  return text
    .toLowerCase()
    .replaceAll("dž", "џ")
    .replaceAll("dj", "ђ")
    .replaceAll("lj", "љ")
    .replaceAll("nj", "њ")
    .replace(/[a-zčćšđž]/g, (letter) => latin[letter] || "")
    .replace(/[^абвгдђежзијклљмнњопрстћуфхцчџш]/g, "")
    .slice(0, WORD_LENGTH);
}

