insert into public.players (nickname)
select candidate.nickname
from (
  select btrim(nickname) as nickname from public.scores
  union
  select btrim(nickname) as nickname from public.normal_stats
  union
  select btrim(creator) as nickname from public.challenges
  union
  select btrim(opponent) as nickname from public.challenges
) as candidate
where candidate.nickname is not null
  and candidate.nickname <> ''
  and lower(candidate.nickname) <> lower('Чека се')
  and not exists (
    select 1
    from public.players existing
    where lower(btrim(existing.nickname)) = lower(candidate.nickname)
  );

