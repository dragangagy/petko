create table if not exists public.scores (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  nickname text not null,
  score integer not null default 0, -- daily score
  attempts integer not null default 0,
  wins integer not null default 0,
  streak integer not null default 0
);

alter table public.scores enable row level security;

drop policy if exists "scores_read" on public.scores;
create policy "scores_read"
on public.scores
for select
to anon
using (true);

drop policy if exists "scores_insert" on public.scores;
create policy "scores_insert"
on public.scores
for insert
to anon
with check (true);

create table if not exists public.challenges (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  accepted_at timestamptz,
  code text not null unique,
  day date not null default current_date,
  creator text not null,
  creator_device text,
  opponent text,
  opponent_device text,
  status text not null default 'pending',
  words jsonb not null,
  creator_score integer,
  creator_attempts integer,
  creator_solved integer,
  creator_played_at timestamptz,
  opponent_score integer,
  opponent_attempts integer,
  opponent_solved integer,
  opponent_played_at timestamptz
);

alter table public.challenges enable row level security;

drop policy if exists "challenges_read" on public.challenges;
create policy "challenges_read"
on public.challenges
for select
to anon
using (true);

drop policy if exists "challenges_insert" on public.challenges;
create policy "challenges_insert"
on public.challenges
for insert
to anon
with check (true);

drop policy if exists "challenges_update" on public.challenges;
